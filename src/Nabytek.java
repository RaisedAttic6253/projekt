public class Nabytek {
    private String jmeno;
    private int cislo;

    public Nabytek(String jmeno, int cislo) {
        this.jmeno = jmeno;
        this.cislo = cislo;
    }

    public String getJmeno() {
        return jmeno;
    }

    public void setJmeno(String jmeno) {
        this.jmeno = jmeno;
    }

    public int getCislo() {
        return cislo;
    }

    public void setCislo(int cislo) {
        this.cislo = cislo;
    }
}

